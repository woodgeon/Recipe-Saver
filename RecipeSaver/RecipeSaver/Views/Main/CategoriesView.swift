struct CategoriesView: View {
    var body: some View {
        NavigationView {
            List {
                ForEach(Category.allCases) { category in
                    NavigationLink {
                        CategoryView(category: category)
                    } label: {
                        Text(category.rawValue)
                    }
                }
            }
            .navigationTitle("카테고리")
        }
        .navigationViewStyle(.stack)
    }
}

